// 한글 초성 추출
export function getChoseong(char: string): string {
  const code = char.charCodeAt(0) - 0xAC00
  if (code < 0 || code > 11171) return ''
  const idx = Math.floor(code / 588)
  return CHOSEONG_LIST[idx]
}
