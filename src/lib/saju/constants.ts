// ── 시간 → 지지 인덱스 매핑 ─────────────────────────
// 23:00~00:59=자(0), 01:00~02:59=축(1), ...
export function hourToJijiIdx(hour: number): number {
  return Math.floor(((hour + 1) % 24) / 2)
}

