// 두 오행 관계 판별
export function ohaengRelation(a: Ohaeng, b: Ohaeng): '상생' | '비화' | '상극' | '역생' | '역극' {
  if (a === b) return '비화'
  if (OHAENG_SANGSAENG[a] === b) return '상생'   // a가 b를 생
  if (OHAENG_SANGSAENG[b] === a) return '역생'   // b가 a를 생
  if (OHAENG_SANGGEUK[a] === b) return '상극'   // a가 b를 극
  return '역극'                                   // b가 a를 극
}

// ── 수리 오행 (끝자리 기준) ─────────────────────────
export function suriToOhaeng(n: number): Ohaeng {
  const d = n % 10
  if (d === 1 || d === 2) return '목'
  if (d === 3 || d === 4) return '화'
  if (d === 5 || d === 6) return '토'
  if (d === 7 || d === 8) return '금'
  return '수' // 9, 0
}

